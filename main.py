@app.route('/register', methods=['POST', 'GET'])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        hashed_salted_password = generate_password_hash(form.password.data, method='pbkdf2:sha256', salt_length=8)
        Email = form.email.data

        if User.query.filter_by(email=Email).first():
            flash("You've already signed up with that email, log in instead.")
            return redirect(url_for('login'))

        new_user = User(email=Email, password=hashed_salted_password, name=form.name.data)

        db.session.add(new_user)
        db.session.commit()

        login_user(new_user)
        return redirect(url_for('get_all_posts'))

    return render_template("register.html", form=form)


@app.route('/login', methods=['POST', 'GET'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data

        user = User.query.filter_by(email=email).first()
        if user:
            # Email exist and password is correct
            if check_password_hash(user.password, password):
                login_user(user)
                return redirect(url_for('get_all_posts'))
            else:
                # Password not correct
                flash('Password incorrect, please try again.')
                return redirect(url_for('login'))

        else:
            # Email not correct
            flash("That email does not exist, please try again")
            return redirect(url_for('login'))

    return render_template("login.html", form=form)


